package com.operators;

public class RelationalOperators {
  public static void main(String[] args) {
	
	  
	  
	  int a = 10;
	  int b = 5;
	  boolean greaterThan = a > b;   // greater than
	  boolean lessThan = a < b;     // less than
	  boolean equalTo = a == b;     // equal to
	  boolean notEqualTo = a != b;              // not equal to
	  boolean greaterThanOrEqualTo = a >= b;    // greater than or equal to
	  boolean lessThanOrEqualTo = a <= b;       // less than or equal to
	  
	  
	  System.out.println(greaterThan);
	  System.out.println(lessThan);
	  System.out.println(equalTo);
	  System.out.println(notEqualTo);
	  System.out.println(greaterThanOrEqualTo);
	  System.out.println(lessThanOrEqualTo);
	  
}
}
